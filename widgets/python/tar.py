def compress():
	files = _.isData()
	gz = _.switches.isActive('Gz')
	tarball = None
	if len(_.switches.value('Compress')):
		tarball = _.switches.values('Compress')[0]
	ext = None
	if _.switches.isActive('NoExt'):
		ext = 0
	_tar.tar( files, tarball, gz, ext  )


def uncompress():
	files = _.isData(r=1)
	_tar.untar(files)


def action():
	subjects=_.isData()
	if _.switches.isActive('Files'):
		subjects=_.switches.values('Files')
	if _.switches.isActive('List'):
		_tar.list(subjects)
		return None

	if not _.switches.isActive('Compress') and not _.switches.isActive('Uncompress'):
		for i,path in enumerate( subjects ):
			# _.pr('here')
			if  path.endswith('.gz') or path.endswith('.bz2'):
				_.switches.fieldSet( 'Uncompress', 'active', True )
			else:
				if os.path.isfile(path) and ( _.isTar.gz(path) or _.isTar.bz2(path) ):
					_.switches.fieldSet( 'Uncompress', 'active', True )
				else:
					_.switches.fieldSet( 'Compress', 'active', True )

	if _.switches.isActive('Compress'):
		compress()
	if _.switches.isActive('Uncompress'):
		uncompress()

