def liner(strin):
	strin=strin.replace('\t','')
	def spa(rin,what): return rin.replace(' '+what,what).replace(what+' ',what);
	strin=_str.do('dup',strin,' ')
	strin=_str.do('be',strin,' ')
	strin=spa(strin,';')
	strin=spa(strin,'=')
	if '="' in strin:
		strin=spa(strin,'"')
	elif "='" in strin:
		strin=spa(strin,"'")
	return strin

def recMe(alias):
	alias=alias[6:]
	label=alias.split('=')[0]
	content=alias.split('=')[1]
	if content.startswith('"'):
		content=content[1:]
	if content.endswith('"'):
		content=content[:-1]
	if content.endswith("'"):
		content=content[:-1]
	# content=content.replace(';','\n')
	return { label:content }

