import os
import zipfile

def zip_file(file_path, archive_name):
	# Check if the specified file exists
	if not os.path.isfile(file_path):
		print(f"The specified file {file_path} does not exist.")
		return
	
	# Create a ZipFile object in write mode
	with zipfile.ZipFile(archive_name, 'w', zipfile.ZIP_DEFLATED, compresslevel=9) as zipf:
		# Add the file to the zip file
		zipf.write(file_path, arcname=os.path.basename(file_path))
	
	# print(f"File {file_path} has been zipped successfully as {archive_name}")
	print(f"{archive_name}")

def zip_folder(folder_path, archive_name):
	# Check if the specified folder exists
	if not os.path.isdir(folder_path):
		print(f"The specified folder {folder_path} does not exist.")
		return
	
	# Create a ZipFile object in write mode
	with zipfile.ZipFile(archive_name, 'w', zipfile.ZIP_DEFLATED, compresslevel=9) as zipf:
		# Walk the directory and add files to the zip file
		for foldername, _, filenames in os.walk(folder_path):
			for filename in filenames:
				# Create complete filepath of file in directory
				file_path = os.path.join(foldername, filename)
				# Add file to zip file
				zipf.write(file_path, arcname=os.path.relpath(file_path, folder_path))
	
	# print(f"Folder {folder_path} has been zipped successfully as {archive_name}")
	print(f"{archive_name}")

def zip(path, archive_name):
	_v.mkdir(archive_name,f=1)
	if os.path.isfile(path):
		zip_file(path, archive_name)
	elif os.path.isdir(path):
		zip_folder(path, archive_name)
	else:
		print(f"The specified path {path} does not exist.")

