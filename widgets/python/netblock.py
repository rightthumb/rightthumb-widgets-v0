import ipaddress

def validate_ip(ip):
	try:
		ipaddress.ip_address(ip)
		return True
	except ValueError:
		return False


import socket

def resolve_domain(domain):
	try:
		return socket.gethostbyname(domain)
	except socket.gaierror:
		return domain

def get_netblock_owner(data):
	data = data.replace('\n', '\\n')

	simplejson=__.imp('simplejson')
	# Parse the JSON data
	json_data = simplejson.loads(data)

	# Get the inetnums array
	inetnums = json_data['result']['inetnums']

	# Loop through the inetnums array
	for inetnum in inetnums:
		# Check if the org object exists and has a name property
		if 'org' in inetnum and 'name' in inetnum['org']:
			# Return the name of the company
			return inetnum['org']['name']

	# If no company name was found, return None
	return None

