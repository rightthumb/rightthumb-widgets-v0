def process( db, sql ):
	global index
	conn = sqlite3.connect(db)
	c = conn.cursor()
	c.execute(sql)
	records = c.fetchall()
	names = getNames(c)
	for f in records:
		record = buildRecord( names, f )
		ext = record['ext'].lower()
		if len(ext) > 0 and len(ext) < 8:
			if not os.path.isfile( record['path'] ):
				sql_del = "   DELETE FROM files WHERE path = '"+record['path']+"';   "
				c.execute(sql_del)
				_.pr( 'DEL', record['path'] )
			else:
				# _.printVarSimple(record)
				# _.pr( record )

				mime = isWhat( record['path'] )

				if not 'Error' == mime:
					if not ext in index:
						index[ext] = {}
					if not mime in index[ext]:
						index[ext][mime] = {}

