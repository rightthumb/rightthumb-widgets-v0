import sqlite3
import _rightThumb._mimetype as _mime

def isWhat(file):
	return _mime.what(file)

def getNames(conn):
	conn.row_factory = sqlite3.Row
	cursor = conn.execute('select * from files')
	row = cursor.fetchone()
	names = row.keys()
	return names

def buildRecord( names, f ):
	row = {}
	for i,n in enumerate(names):
		row[n] = f[i]
	return row


